num1 = int(input("Enter first number: ")) # this  collects a number from the user
num2 = int(input("Enter second number: ")) # this collects another number feom the user

print(f"{num1} == {num2} : {num1 == num2}")
# the output will be true if num1 is equal to num2 otherwise false
print(f"{num1} != {num2} : {num1 != num2}")
# the output will be true if num1 is  not equal to num2 otherwise false
print(f"{num1} > {num2} : {num1 > num2}")
# the output will be true if num1 is  greater than  num2 otherwise false
print(f"{num1} < {num2} : {num1 < num2}")
# the output will be true if num1 is  less than  num2 otherwise false

